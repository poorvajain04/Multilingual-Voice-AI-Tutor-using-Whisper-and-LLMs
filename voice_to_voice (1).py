from gtts import gTTS
from langdetect import detect

def text_to_speech(text):

    try:
        language = detect(text)
    except:
        language = "en"

    output_file = "reply.mp3"

    tts = gTTS(
        text=text,
        lang=language,
        slow=False
    )

    tts.save(output_file)

    return output_file


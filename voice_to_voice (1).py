def ai_tutor(user_text):

    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",

        messages=[
            {
                "role": "system",
                "content": SYSTEM_PROMPT
            },
            {
                "role": "user",
                "content": user_text
            }
        ],

        temperature=0.7,
        max_tokens=300
    )

    return response.choices[0].message.content

